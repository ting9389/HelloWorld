public class ChangeChar{
	
	public static void main(String[] arg) {
		//	\t:一个制位表，可实现对齐的功能
		System.out.println("北京\t天津\t上海");
		//	\n:换行符
		System.out.println("猫\nです。");
		//	\\:一个\
		System.out.println("http:\\www");
		//	\\\\:两个\\
		System.out.println("http:\\\\goole.com\\\\");
		//	\":一个"
		System.out.println("java学习\"");
		//	\':一个'
		System.out.println("xuexi'");
		//	\r:一个回车
		System.out.println("社长在直播\r我们");
		
	}
}